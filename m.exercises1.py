#With type command you can see the type of a variable in python 
# eg: type(1) print each components type with this way you can see most significant data types in python
print(type('selam'))
print(type([1,2,3,'selam']))
print(type(True))
print(type({'honda':'civic','volvo':'s90'}))
print(type(('osman','orhan','mustafa',1961,1964,1969)))
print(type(63))
print(type(40.9))
print(type(20 + 5j))
print(type(['zeynep','ayse','rabia']))
print(type({'zeynep','ayse','rabia'}))
